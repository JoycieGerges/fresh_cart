import { WishlistState } from '@/app/web/wishlist/Store/Slice.wishlist';
import Providers from "@/components/providers/providers";
import { verifyToken } from "@/Cookies/auth.actions";
import { getLoggedUserCart } from "@/APIs/cart";
import { GetLoggedUserWishlist } from "@/APIs/whishlist";
import type { Metadata } from "next";
import { Exo, Geist, Geist_Mono } from "next/font/google";
import '../../node_modules/@fortawesome/fontawesome-free/css/all.min.css';
import "../css/style.css";
import { CartState } from "./web/cart/Store/slice.cart";
import Footer from "./components2/footer/footer";
import Navbar from "./components2/navbar/navbar";

const exo = Exo({
  subsets:['latin'],
  weight:['400','500','600','700','800','900'],
  variable:'--font-exo'
})

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Fresh Cart",
  description: "Generated by create next app",
};

let cartState:CartState = {
  cartId: null,
  products: [],
  totalCartPrice: 0,
  numOfCartItems: 0,
  error: null,
  IsLoading: false,
}
let wishlistState:WishlistState={
    status: "success",
    count: 0,
    data: [] ,
}



export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {

  const response = await verifyToken();
  if(response.isAuthenticated){
    const cart = await getLoggedUserCart();
    cartState={
      cartId: cart.cartId,
      products: cart.data.products,
      totalCartPrice: cart.data.totalCartPrice,
      numOfCartItems: cart.numOfCartItems,
      error: null,
      IsLoading: false,
    }
    const wishlist = await GetLoggedUserWishlist();
    wishlistState={
      status: wishlist.status,
      count: wishlist.count,
      data: wishlist.data,
    }

  }

  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased ${exo.className}`}
        cz-shortcut-listen=""
      >
          <Providers preloadedState={{auth:response, cart:cartState,wishlist:wishlistState}}>
            <Navbar/>
            {children}
            <Footer/>
          </Providers>

          
      </body>
    </html>
  );
}
